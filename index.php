        <h5>DESARROLLADO POR:</h5>
        <span>22 Junio 2024</span>
        <p>Somos estudiantes de Ingeniería en Sistemas de la Universidad Gabriel René Moreno, en la Facultad Integral de Ichilo. Estamos desarrollando esta plataforma web con el objetivo de conectar talentos con oportunidades laborales en Yapacaní.</p>
        <p>Nuestra misión es facilitar el proceso de búsqueda de empleo tanto para los candidatos como para las empresas, creando un espacio virtual donde puedan encontrarse y colaborar.</p>
        <p>Este proyecto es especialmente relevante para nuestra comunidad, ya que Yapacaní tiene un gran potencial de crecimiento y desarrollo. Creemos firmemente en el poder de la tecnología para mejorar la empleabilidad y la accesibilidad a trabajos dignos, lo que es crucial para el progreso local.</p>
        <p>Con esta iniciativa, aspiramos a contribuir al desarrollo económico y social de nuestra región, brindando herramientas modernas y eficientes para que todos puedan acceder a mejores oportunidades laborales.</p>
